package Calcular;

public class Commodities {
	  private double preco;
	  private String Qualidade;
	  private double Quantidade;
	  private double custoFabricacao;
	  private double custoMateriaPrima;
	  private double custoMaoDeObra;
	  private double custoMaquinas;
	  
	  
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public String getQualidade() {
		return Qualidade;
	}
	public void setQualidade(String qualidade) {
		Qualidade = qualidade;
	}
	public double getQuantidade() {
		return Quantidade;
	}
	public void setQuantidade(double quantidade) {
		Quantidade = quantidade;
	}
	public double getCustoFabricacao() {
		return custoFabricacao;
	}
	public void setCustoFabricacao(double custoFabricacao) {
		this.custoFabricacao = custoFabricacao;
	}
	public double getCustoMateriaPrima() {
		return custoMateriaPrima;
	}
	public void setCustoMateriaPrima(double custoMateriaPrima) {
		this.custoMateriaPrima = custoMateriaPrima;
	}
	public double getCustoMaoDeObra() {
		return custoMaoDeObra;
	}
	public void setCustoMaoDeObra(double custoMaoDeObra) {
		this.custoMaoDeObra = custoMaoDeObra;
	}
	public double getCustoMaquinas() {
		return custoMaquinas;
	}
	public void setCustoMaquinas(double custoMaquinas) {
		this.custoMaquinas = custoMaquinas;
	}
	  
	  

}
